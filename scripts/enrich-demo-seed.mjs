#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const seedPath = path.join(root, 'src/data/tenantDemoSeed.ts');

// Read current seed
const content = fs.readFileSync(seedPath, 'utf-8');

// Extract JSON - find the opening { and closing }
const startMatch = content.match(/export const TENANT_DEMO_SEED = (\{)/);
if (!startMatch) {
  console.error('Could not find TENANT_DEMO_SEED start');
  process.exit(1);
}

// Find matching closing brace - go backwards from "as const"
const asConstIdx = content.lastIndexOf('as const');
let braceCount = 0;
let endIdx = asConstIdx - 1;
let closingBraceIdx = -1;

// Scan backwards to find matching }
for (let i = asConstIdx - 1; i >= 0; i--) {
  if (content[i] === '}') {
    closingBraceIdx = i;
    break;
  }
}

if (closingBraceIdx === -1) {
  console.error('Could not find closing brace');
  process.exit(1);
}

const startIdx = content.indexOf('{', content.indexOf('TENANT_DEMO_SEED'));
const jsonStr = content.substring(startIdx, closingBraceIdx + 1);

// Parse JSON
let data;
try {
  data = JSON.parse(jsonStr);
} catch (e) {
  console.error('JSON parse error:', e.message);
  console.error('Trying substring:', jsonStr.substring(0, 200));
  process.exit(1);
}

// Define complete addresses (15 unique locations across Vietnam)
const addresses = [
  '123 QL1A, Ninh Hòa, Khánh Hòa',
  '456 QL1A, Cam Ranh, Khánh Hòa',
  '789 QL1A, Nha Trang, Khánh Hòa',
  '101 QL1A, Phan Rang, Ninh Thuận',
  '202 QL1A, Tuy Hòa, Phú Yên',
  '303 QL1A, Quy Nhơn, Bình Định',
  '404 QL1A, Pleiku, Gia Lai',
  '505 QL1A, Kon Tum, Kon Tum',
  '606 QL1A, Buôn Ma Thuột, Đắk Lắk',
  '707 QL1A, Gia Nghĩa, Đắk Nông',
  '808 QL1A, Đà Nẵng',
  '909 QL1A, Huế, Thừa Thiên Huế',
  '1010 QL1A, Đồng Nai',
  '1111 QL1A, TP.HCM',
  '1212 QL1A, Bình Dương'
];

// Enrich drivers with address and health_check_expiry
data.collections.drivers.forEach((driver, idx) => {
  // Add address if missing
  if (!driver.address) {
    driver.address = addresses[idx % addresses.length];
  }
  
  // Add health_check_expiry if missing
  if (!driver.health_check_expiry) {
    const year = 2027 + Math.floor(idx / 12);
    const month = (idx % 12) + 1;
    const monthStr = String(month).padStart(2, '0');
    driver.health_check_expiry = `${year}-${monthStr}-15`;
  }
});

// Update metadata
data.metadata.modified_at = new Date().toISOString();
data.metadata.enriched_fields = ['address', 'health_check_expiry'];

// Convert back to string
const jsonStr2 = JSON.stringify(data, null, 2);
const newContent = `/* eslint-disable */
// Auto-generated by scripts/generate-tenant-demo-seed.mjs
// Do not edit manually.

export const TENANT_DEMO_SEED = ${jsonStr2} as const;
`;

fs.writeFileSync(seedPath, newContent, 'utf-8');
console.log(`✅ Enriched ${data.collections.drivers.length} drivers with address & health_check_expiry`);
